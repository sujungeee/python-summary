# 3. 튜플 다루기
#   - 튜플은 요솟값을 변경할 수 없기 때문에
#     sort, insert, remove, pop과 같은 내장 함수가 없다.
t1 = (1, 2, 'a', 'b')
    # 인덱싱하기
print(t1[0]) # 1
print(t1[3]) # b

    # 슬라이싱하기
print(t1[1:]) # (2, 'a', 'b')

    # 튜플 더하기
t2= (3,4)
t3= t1+t2
print(t3) # (1, 2, 'a', 'b', 3, 4)

    # 튜플 곱하기
t3= t2*3
print(t3) # (3, 4, 3, 4, 3, 4)

    # 튜플 길이 구하기
print(len(t1)) # 4