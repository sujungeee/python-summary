# 1. 튜플은 어떻게 만들까?
t1 = ()
t2 = (1,) # 튜플이 단 한 개의 요소만 갖는다면 값 뒤에 ,(쉼표)를 무조건 붙여야 함
t3 = (1, 2, 3)
t4 = 1, 2, 3 # 소괄호 생략해도 ㅇㅋ
t5 = ('a', 'b', ('ab', 'cd'))
print(t1) # ()
print(t2) # (1,)
print(t3) # (1, 2, 3)
print(t4) # (1, 2, 3)
print(t5) # ('a', 'b', ('ab', 'cd'))

